#include <stdio.h>
void main()
{
	int y1,y2;            /*定义变量Y1和Y2为INT型,分别 用来表示两个年份*/
	printf("输入第一个年份=");    /*提示输入*/
	scanf("%hd",&y1);              //停顿,并等待用户从键盘输入第一个年份,送给Y1
	printf("输入第二个年份=");    //提示输入
	scanf("%hd",&y2);              //停顿,并等待用户从键盘输入第二个年份,送给Y2
	printf("你输入的年份是%hd,%hd,两者相差%d年\n",y1,y2,y1-y2);        //输出结果
}          